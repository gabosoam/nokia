CREATE DEFINER=`root`@`localhost` PROCEDURE `p_billprice`(IN `bill` int)
BEGIN
	SELECT
model.`code`,
model.description,
product.variant AS var,
product.bill AS bi,
product.bill,
(SELECT COUNT(*) FROM product WHERE product.variant= var AND product.bill=bi) AS count,
product.price,
model.id,
((SELECT COUNT(*) FROM product WHERE product.variant= var AND product.bill=bi)*product.price )as total
FROM
product
INNER JOIN model ON product.variant = model.id
WHERE product.bill = bill
GROUP BY description, bill;

END
